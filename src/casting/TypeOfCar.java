package casting;

public class TypeOfCar {
    public TypeOfCar() {
        System.out.println("Running no-arg constructor in TypeOfCar");
    }
    public void categories() { System.out.println("Cars have different categories like sedan, SUV, hatchback"); }
    public void fuelType() { System.out.println("Cars use petrol, diesel, or electricity"); }
    public void speed() { System.out.println("Different cars have different speed limits"); }
    public void brands() { System.out.println("Cars are manufactured by various brands"); }
    public void seatingCapacity() { System.out.println("Cars have different seating capacities"); }
}

