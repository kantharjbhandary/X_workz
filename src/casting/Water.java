package casting;

public class Water {
    public Water() {
        System.out.println("Running no-arg constructor in Water");
    }

    public void necessity() { System.out.println("Water is essential for life."); }
    public void sources() { System.out.println("Comes from rivers, lakes, and underground sources."); }
    public void states() { System.out.println("Exists as solid, liquid, and gas."); }
    public void purification() { System.out.println("Water can be purified for safe drinking."); }
    public void usage() { System.out.println("Used in drinking, agriculture, and industry."); }
}

