package casting;

public class Color {
    public Color() {
        System.out.println("Running no-arg constructor in Color");
    }
    public void shades() { System.out.println("Colors have different shades"); }
    public void hexCode() { System.out.println("Each color has a unique hex code"); }
    public void usage() { System.out.println("Colors are used in various industries"); }
    public void perception() { System.out.println("Colors can affect mood and perception"); }
    public void contrast() { System.out.println("Colors have different contrast levels"); }
}