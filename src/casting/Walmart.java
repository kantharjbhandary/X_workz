package casting;

public class Walmart {
    public Walmart() {
        System.out.println("Running no-arg constructor in Walmart");
    }
    public void branches() { System.out.println("Walmart has branches worldwide"); }
    public void products() { System.out.println("Walmart sells various products"); }
    public void services() { System.out.println("Walmart offers multiple services"); }
    public void discounts() { System.out.println("Walmart provides seasonal discounts"); }
    public void eCommerce() { System.out.println("Walmart has an online shopping platform"); }
}