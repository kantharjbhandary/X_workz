package casting;
public class Work {
    public Work() {
        System.out.println("Running no-arg constructor in Work");
    }
    public void type() { System.out.println("Work can be physical or mental"); }
    public void environment() { System.out.println("Work is done in different environments"); }
    public void salary() { System.out.println("Some work is paid, some voluntary"); }
    public void industry() { System.out.println("Work exists in various industries"); }
    public void hours() { System.out.println("Work can have flexible or fixed hours"); }
}