package casting;

public class Galaxy {
    public Galaxy() {
        System.out.println("Running no-arg constructor in Galaxy");
    }
    public void composition() { System.out.println("Galaxies are made up of stars, planets, and dust"); }
    public void types() { System.out.println("Galaxies can be spiral, elliptical, or irregular"); }
    public void distance() { System.out.println("Galaxies are located at vast distances"); }
    public void discovery() { System.out.println("Galaxies are studied using telescopes"); }
    public void size() { System.out.println("Galaxies vary in size and number of stars"); }
}
