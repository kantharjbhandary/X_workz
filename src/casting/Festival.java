package casting;

public class Festival {
    public Festival() {
        System.out.println("Running no-arg constructor in Festival");
    }

    public void name() {
        System.out.println("Festival has a specific name");
    }

    public void culture() {
        System.out.println("Festival represents a culture");
    }

    public void celebrationType() {
        System.out.println("Festival is celebrated in different ways");
    }

    public void publicHoliday() {
        System.out.println("Some festivals are public holidays");
    }

    public void foodSpecialties() {
        System.out.println("Festivals have special food items");
    }
}
