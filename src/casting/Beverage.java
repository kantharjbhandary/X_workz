package casting;

public class Beverage {
    public Beverage() {
        System.out.println("Running no-arg constructor in Beverage");
    }
public void temperature() { System.out.println("Beverages can be hot or cold"); }
public void flavor() { System.out.println("Beverages come in different flavors"); }
public void ingredients() { System.out.println("Beverages are made from various ingredients"); }
public void packaging() { System.out.println("Beverages have different packaging types"); }
public void brand() { System.out.println("Beverages come from different brands"); }
}
