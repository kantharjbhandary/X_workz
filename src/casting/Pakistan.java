package casting;

public class Pakistan {
    public Pakistan() {
        System.out.println("Running no-arg constructor in Pakistan");
    }

    public void geography() { System.out.println("Located in South Asia."); }
    public void capital() { System.out.println("Capital city is Islamabad."); }
    public void language() { System.out.println("Official language is Urdu."); }
    public void economy() { System.out.println("Economy is based on agriculture and manufacturing."); }
    public void culture() { System.out.println("Pakistan has a rich cultural heritage."); }
}