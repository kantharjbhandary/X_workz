package casting;

public class Devil {
    public Devil() {
        System.out.println("Running no-arg constructor in Devil");
    }
    public void evil() { System.out.println("Devil represents evil"); }
    public void trickery() { System.out.println("Devil deceives people"); }
    public void darkness() { System.out.println("Devil is associated with darkness"); }
    public void myths() { System.out.println("Devil appears in myths and folklore"); }
    public void fear() { System.out.println("Devil instills fear in people"); }
}
