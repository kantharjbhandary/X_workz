package casting;

public class Hospital {
    public Hospital() {
        System.out.println("Running no-arg constructor in Hospital");
    }
    public void departments() { System.out.println("Hospitals have different departments"); }
    public void staff() { System.out.println("Hospitals have medical and administrative staff"); }
    public void facilities() { System.out.println("Hospitals provide various facilities"); }
    public void emergency() { System.out.println("Hospitals handle medical emergencies"); }
    public void location() { System.out.println("Hospitals are located in various regions"); }
}