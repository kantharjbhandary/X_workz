package casting;

public class Keyboard {
    public Keyboard() {
        System.out.println("Running no-arg constructor in Keyboard");
    }
    public void type() { System.out.println("Keyboards can be mechanical or membrane"); }
    public void connectivity() { System.out.println("Keyboards can be wired or wireless"); }
    public void backlight() { System.out.println("Some keyboards have RGB lighting"); }
    public void keys() { System.out.println("Keyboards have different types of keys"); }
    public void brand() { System.out.println("Keyboards come from different brands"); }
}

