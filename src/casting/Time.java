package casting;

public class Time {
    public Time() {
        System.out.println("Running no-arg constructor in Time");
    }
    public void measurement() { System.out.println("Time is measured in seconds, minutes, and hours"); }
    public void concept() { System.out.println("Time is a fundamental concept"); }
    public void zones() { System.out.println("Different regions have different time zones"); }
    public void relativity() { System.out.println("Time is relative, as per Einstein's theory"); }
    public void importance() { System.out.println("Time management is crucial in life"); }
}