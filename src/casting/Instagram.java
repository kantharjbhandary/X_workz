package casting;

public class Instagram {
    public Instagram() {
        System.out.println("Running no-arg constructor in Instagram");
    }
    public void posts() { System.out.println("Users share photos and videos"); }
    public void stories() { System.out.println("Instagram allows temporary stories"); }
    public void reels() { System.out.println("Instagram has short video reels"); }
    public void followers() { System.out.println("Users can follow others"); }
    public void filters() { System.out.println("Instagram provides various filters"); }
}
