package casting;

public class YouTube {
    public YouTube() {
        System.out.println("Running no-arg constructor in YouTube");
    }
    public void contentType() { System.out.println("YouTube hosts various types of content"); }
    public void users() { System.out.println("YouTube has millions of users"); }
    public void monetization() { System.out.println("YouTube allows monetization"); }
    public void quality() { System.out.println("Videos are available in different qualities"); }
    public void platform() { System.out.println("YouTube is accessible on multiple platforms"); }
}
