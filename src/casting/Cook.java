package casting;

public class Cook {
    public Cook() {
        System.out.println("Running no-arg constructor in Cook");
    }
    public void skills() { System.out.println("Cooks have various cooking skills"); }
    public void ingredients() { System.out.println("Cooking requires different ingredients"); }
    public void methods() { System.out.println("Cooking involves different methods"); }
    public void cuisine() { System.out.println("Cooking is influenced by different cuisines"); }
    public void tools() { System.out.println("Cooking requires various tools and utensils"); }
}