package casting;

public class Toothpaste {
    public Toothpaste() {
        System.out.println("Running no-arg constructor in Toothpaste");
    }
    public void ingredients() { System.out.println("Toothpaste contains fluoride and other ingredients"); }
    public void flavor() { System.out.println("Toothpaste comes in different flavors"); }
    public void brand() { System.out.println("Toothpaste is available from various brands"); }
    public void benefits() { System.out.println("Toothpaste helps in maintaining oral hygiene"); }
    public void usage() { System.out.println("Toothpaste should be used twice daily"); }
}