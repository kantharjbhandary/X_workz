package casting;

public class Bag {
    public Bag() {
        System.out.println("Running no-arg constructor in Bag");
    }

    public void carryItems() {
        System.out.println("Bag is used to carry items");
    }

    public void materialType() {
        System.out.println("Bag is made of different materials");
    }

    public void size() {
        System.out.println("Bag comes in various sizes");
    }

    public void durability() {
        System.out.println("Bag has different durability levels");
    }

    public void compartments() {
        System.out.println("Bag has multiple compartments for storage");
    }
}