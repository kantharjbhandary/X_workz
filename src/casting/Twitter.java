package casting;
public class Twitter {
    public Twitter() {
        System.out.println("Running no-arg constructor in Twitter");
    }
    public void tweets() { System.out.println("Users post tweets on Twitter"); }
    public void retweets() { System.out.println("Tweets can be retweeted"); }
    public void trending() { System.out.println("Twitter shows trending topics"); }
    public void followers() { System.out.println("Users can follow each other"); }
    public void verification() { System.out.println("Some accounts are verified"); }
}