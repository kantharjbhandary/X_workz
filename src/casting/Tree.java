package casting;

public class Tree {
    public Tree() {
        System.out.println("Running no-arg constructor in Tree");
    }
    public void type() { System.out.println("Trees can be deciduous or evergreen"); }
    public void height() { System.out.println("Trees grow to different heights"); }
    public void leaves() { System.out.println("Trees have different types of leaves"); }
    public void lifespan() { System.out.println("Trees have varying lifespans"); }
    public void uses() { System.out.println("Trees provide wood, oxygen, and shade"); }
}