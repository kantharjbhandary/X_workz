package casting;

public class Plug {
    public Plug() {
        System.out.println("Running no-arg constructor in Plug");
    }

    public void function() { System.out.println("Plugs connect electrical devices to power sources."); }
    public void types() { System.out.println("Includes 2-pin and 3-pin plugs."); }
    public void safety() { System.out.println("Some plugs have built-in surge protection."); }
    public void material() { System.out.println("Made of plastic and metal conductors."); }
    public void voltage() { System.out.println("Different plugs support different voltage levels."); }
}
